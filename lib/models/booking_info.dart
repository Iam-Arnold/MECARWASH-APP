class BookingInfo {
  String customerName;
  String phone;
  String address;
  String carMake;
  String carColor;
  String carModel;
  String plateNumber;
  String instruction;
  String service;
  DateTime date;
  String timeSlot;
  String email;

  BookingInfo({
    required this.customerName,
    required this.phone,
    required this.address,
    required this.carMake,
    required this.carColor,
    required this.carModel,
    required this.plateNumber,
    required this.instruction,
    required this.service,
    required this.date,
    required this.timeSlot,
    required this.email,
  });
}
